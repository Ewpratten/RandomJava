public class StickFigure{
  public static void main(String[] args){
    System.out.println(" \\(\")/");
    System.out.println("  ==");
    System.out.println("  ||");
    System.out.println("  /\\");
    System.out.println("_/  \\_");
    
    // In a single line
    System.out.print("\n\n  \\(\")/\n   ==\n   ||\n   /\\\n _/  \\_\n");
    return;
  }
}